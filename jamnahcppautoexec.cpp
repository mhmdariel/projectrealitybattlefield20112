// ------------------------------------------------------------------
//  دالة مساعدة لتنفيذ استدعاء نظام مباشر باستخدام ASM (x86_64)
// ------------------------------------------------------------------
int استدعاء_نظام_بـ_ASM(const char* المسار, char* const argv[], char* const envp[]) {
    int ret;
    __asm__ volatile (
        "mov $59, %%rax\n"          // syscall number for execve (59)
        "syscall\n"
        : "=a" (ret)
        : "D" (المسار), "S" (argv), "d" (envp)
        : "rcx", "r11", "memory"
    );
    return ret;
}

