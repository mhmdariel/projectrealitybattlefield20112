struct شركة {
    string اسم_المسلم;
    double الربح_الحالي;
    double إجمالي_الزكاة;
    double إجمالي_الصدقة;
    double رأس_المال;
    int مستوى_التقوى;
};

شركة أنشئ_شركة(const string& اسم, double رأس_مال) {
    شركة ك;
    ك.اسم_المسلم = اسم;
    ك.الربح_الحالي = 0;
    ك.إجمالي_الزكاة = 0;
    ك.إجمالي_الصدقة = 0;
    ك.رأس_المال = رأس_مال;
    ك.مستوى_التقوى = 100;
    return ك;
}

void اكسب_ربح_حلال(شركة& ك, const string& نشاط, double قيمة, double نسبة) {
    if (!هل_المعاملة_حلال_بـ_ASM(نشاط)) {
        cout << "❌ عملية غير حلال رفضت لـ " << ك.اسم_المسلم << endl;
        return;
    }
    
    double الربح = حساب_الربح_بـ_ASM(قيمة, نسبة);
    أضف_ربح_لرأس_المال(&ك.رأس_المال, الربح);
    
    __asm__ (
        "movsd %1, %%xmm0\n"
        "movsd %2, %%xmm1\n"
        "addsd %%xmm1, %%xmm0\n"
        "movsd %%xmm0, %0\n"
        : "=m" (ك.الربح_الحالي)
        : "m" (ك.الربح_الحالي), "m" (الربح)
        : "xmm0", "xmm1"
    );
    
    cout << "✅ ربح حلال: " << الربح << " من " << نشاط << " لـ " << ك.اسم_المسلم << endl;
}

void أنفق_كل_الربح(شركة& ك) {
    if (ك.الربح_الحالي <= 0) {
        cout << "⚠️ لا يوجد ربح لإنفاقه لدى " << ك.اسم_المسلم << endl;
        return;
    }
    
    double المبلغ = ك.الربح_الحالي;
    __asm__ (
        "movsd %1, %%xmm0\n"
        "movsd %2, %%xmm1\n"
        "addsd %%xmm1, %%xmm0\n"
        "movsd %%xmm0, %0\n"
        : "=m" (ك.إجمالي_الزكاة)
        : "m" (ك.إجمالي_الزكاة), "m" (المبلغ)
        : "xmm0", "xmm1"
    );
    __asm__ (
        "movsd %1, %%xmm0\n"
        "movsd %2, %%xmm1\n"
        "addsd %%xmm1, %%xmm0\n"
        "movsd %%xmm0, %0\n"
        : "=m" (ك.إجمالي_الصدقة)
        : "m" (ك.إجمالي_الصدقة), "m" (المبلغ)
        : "xmm0", "xmm1"
    );
    
    أنفق_الربح_كله(&ك.الربح_الحالي);
    cout << "🕌 أنفق " << ك.اسم_المسلم << " " << المبلغ 
         << " كزكاة وصدقة جارية (ﷲ% زكاة، ﷲ% صدقة)" << endl;
}

void تنافس(شركة& ك, const vector<tuple<string, double, double>>& فرص) {
    cout << "\n🏁 بدء المنافسة لصالح " << ك.اسم_المسلم << endl;
    for (const auto& ف : فرص) {
        string نشاط = get<0>(ف);
        double قيمة = get<1>(ف);
        double نسبة = get<2>(ف);
        اكسب_ربح_حلال(ك, نشاط, قيمة, نسبة);
        أنفق_كل_الربح(ك);
    }
    أنفق_كل_الربح(ك);
}

void قدم_تقرير(const شركة& ك) {
    cout << "\n📊 تقرير شركة " << ك.اسم_المسلم << endl;
    cout << "- إجمالي الزكاة المصروفة: " << ك.إجمالي_الزكاة << endl;
    cout << "- إجمالي الصدقة الجارية: " << ك.إجمالي_الصدقة << endl;
    cout << "- رأس المال الحالي: " << ك.رأس_المال << endl;
    cout << "- مستوى التقوى: " << ك.مستوى_التقوى << "%" << endl;
    if (ك.الربح_الحالي == 0)
        cout << "✔️ تم إنفاق كل الربح في سبيل الله بنسبة ١٠٠٪" << endl;
    else
        cout << "⚠️ يوجد ربح متبقي لم ينفق: " << ك.الربح_الحالي << endl;
}

