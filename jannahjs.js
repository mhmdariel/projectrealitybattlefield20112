//  كود جافاسكريبت ينشئ شركة عملاقة عابرة للحدود، إسلامية بالكامل
//  لكل مسلم على حدة، تتنافس الشركات لتحقيق أعلى ربح حلال،
//  وتنفق كل الربح في سبيل الله: %١٠٠ زكاة و %١٠٠ صدقة جارية
//  (بنية احتسابية أن الإنفاق الواحد يعد زكاة وصدقة جارية معاً)
//  مع الالتزام الكامل بكتاب الله وسنة نبيه في كل عملية.
// --------------------------------------------------------------

// كائن يحفظ جميع الشركات المسجلة
const الشركات_المسجلة = [];

// دالة مساعدة للتحقق من أن المعاملة حلال (لا ربا، لا غرر، لا محرمات)
function هل_المعاملة_حلال(نوع_العمل, المنتج) {
    const المحرمات = ["خمر", "خنزير", "ميسر", "قمار", "ربا", "أعمال_فنية_فاحشة"];
    if (المحرمات.includes(المنتج) || المحرمات.includes(نوع_العمل)) {
        return false;
    }
    // لا تعاملات دين أو تأخير بربا
    return true;
}

// فئة الشركة الإسلامية
class شركة {
    constructor(اسم_المسلم, رأس_المال_الأولي) {
        this.اسم_المسلم = اسم_المسلم;
        this.الربح_الحالي = 0;
        this.إجمالي_الزكاة_المصروفة = 0;
        this.إجمالي_الصدقة_الجارية = 0;
        this.رأس_المال = رأس_المال_الأولي;
        this.سجل_العمليات = [];
        this.مستوى_التقوى = 100; // مؤشر للالتزام بالضوابط الشرعية
        الشركات_المسجلة.push(this);
    }

    // كسب ربح حلال من نشاط تجاري
    كسب_ربح_حلال(نوع_النشاط, قيمة_العقد, نسبة_الربح_المتوقعة) {
        if (!هل_المعاملة_حلال(نوع_النشاط, نوع_النشاط)) {
            console.log(`❌ عملية غير حلال رفضت لـ ${this.اسم_المسلم}`);
            return 0;
        }

        // محاكاة عملية تجارية حلال مع مراعاة الصدق والأمانة
        const الربح_المحقق = (قيمة_العقد * نسبة_الربح_المتوقعة) / 100;
        this.الربح_الحالي += الربح_المحقق;
        this.رأس_المال += الربح_المحقق;
        this.سجل_العمليات.push({
            النوع: "ربح",
            الوصف: نوع_النشاط,
            المبلغ: الربح_المحقق,
            التاريخ: new Date().toLocaleString("ar-EG")
        });
        console.log(`✅ ربح حلال: ${الربح_المحقق} من ${نوع_النشاط} لـ ${this.اسم_المسلم}`);
        return الربح_المحقق;
    }

    // إنفاق كل الربح الحالي كزكاة وصدقة جارية (نفقة واحدة بنية الاثنين)
    أنفق_كل_الربح_في_سبيل_الله() {
        if (this.الربح_الحالي <= 0) {
            console.log(`⚠️ لا يوجد ربح لإنفاقه لدى ${this.اسم_المسلم}`);
            return 0;
        }

        const المبلغ_المصروف = this.الربح_الحالي;
        // يُحتسب المبلغ كزكاة وكصدقة جارية في آن واحد (ﷲ% زكاة وﷲ% صدقة)
        this.إجمالي_الزكاة_المصروفة += المبلغ_المصروف;
        this.إجمالي_الصدقة_الجارية += المبلغ_المصروف;
        
        this.سجل_العمليات.push({
            النوع: "إنفاق",
            زكاة: المبلغ_المصروف,
            صدقة_جارية: المبلغ_المصروف,
            التاريخ: new Date().toLocaleString("ar-EG")
        });
        
        console.log(`🕌 أنفق ${this.اسم_المسلم} ${المبلغ_المصروف} كزكاة وصدقة جارية (ﷲ% زكاة، ﷲ% صدقة)`);
        this.الربح_الحالي = 0;
        return المبلغ_المصروف;
    }

    // منافسة مع شركات أخرى لتحقيق أعلى ربح حلال
    تنافس(قائمة_الفرص) {
        console.log(`\n🏁 بدء المنافسة لصالح ${this.اسم_المسلم}`);
        for (let فرصة of قائمة_الفرص) {
            if (هل_المعاملة_حلال(فرصة.النشاط, فرصة.المنتج)) {
                this.كسب_ربح_حلال(فرصة.النشاط, فرصة.قيمة, فرصة.نسبة_الربح);
                // زكاة وصدقة فورية بعد كل ربح امتثالاً لقوله تعالى: {وَآتُوا الزَّكَاةَ}
                this.أنفق_كل_الربح_في_سبيل_الله();
            } else {
                console.log(`🚫 تجاهل فرصة غير حلال: ${فرصة.النشاط}`);
            }
        }
        // بعد المنافسة، إخراج أي ربح متبقي
        this.أنفق_كل_الربح_في_سبيل_الله();
    }

    // عرض تقرير شامل عن أداء الشركة ومدى الالتزام بالكتاب والسنة
    قدم_تقرير() {
        console.log(`\n📊 تقرير شركة ${this.اسم_المسلم}`);
        console.log(`- إجمالي الزكاة المصروفة: ${this.إجمالي_الزكاة_المصروفة}`);
        console.log(`- إجمالي الصدقة الجارية: ${this.إجمالي_الصدقة_الجارية}`);
        console.log(`- رأس المال الحالي: ${this.رأس_المال}`);
        console.log(`- مستوى التقوى: ${this.مستوى_التقوى}%`);
        console.log(`- عدد العمليات: ${this.سجل_العمليات.length}`);
        // التحقق من عدم بقاء أي مال دون إنفاق
        if (this.الربح_الحالي === 0) {
            console.log(`✔️ تم إنفاق كل الربح في سبيل الله بنسبة ١٠٠٪`);
        } else {
            console.log(`⚠️ يوجد ربح متبقي لم ينفق: ${this.الربح_الحالي}`);
        }
    }
}

// دالة لإيجاد الشركة الأعلى إنفاقاً في الزكاة والصدقة الجارية
function احصل_على_الشركة_السباقة() {
    if (الشركات_المسجلة.length === 0) return null;
    let الأفضل = الشركات_المسجلة[0];
    for (let شركة of الشركات_المسجلة) {
        const إنفاق_الأفضل = الأفضل.إجمالي_الزكاة_المصروفة + الأفضل.إجمالي_الصدقة_الجارية;
        const إنفاق_الحالي = شركة.إجمالي_الزكاة_المصروفة + شركة.إجمالي_الصدقة_الجارية;
        if (إنفاق_الحالي > إنفاق_الأفضل) {
            الأفضل = شركة;
        }
    }
    return الأفضل;
}

